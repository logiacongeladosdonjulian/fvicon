// Expose translations globally for script.js to access
window._translations = translations;

document.addEventListener('DOMContentLoaded', () => {
    const langSelect = document.getElementById('lang-select');
    
    let savedLang = localStorage.getItem('fvicon_lang');
    if (!savedLang) {
        const browserLang = navigator.language.slice(0, 2);
        savedLang = translations[browserLang] ? browserLang : 'es';
    }
    
    langSelect.value = savedLang;
    updateLanguage(savedLang);

    langSelect.addEventListener('change', (e) => {
        const lang = e.target.value;
        updateLanguage(lang);
        localStorage.setItem('fvicon_lang', lang);
    });

    function updateLanguage(lang) {
        if (!translations[lang]) return;
        const dict = translations[lang];

        // Update all elements with data-i18n attribute (including <option> elements)
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (dict[key]) el.textContent = dict[key];
        });
        
        // Update uploaded state if present
        if (document.querySelector('.success-icon')) {
            const t = document.querySelector('.drag-title');
            const d = document.querySelector('.drag-desc');
            if (t) t.textContent = dict['uploaded_title'];
            if (d) d.textContent = dict['uploaded_desc'];
        }

        // Update the export hint text to match current format
        const formatSelect = document.getElementById('format-select');
        const exportHint   = document.getElementById('export-hint');
        if (formatSelect && exportHint) {
            const hintKeys = { ico: 'hint_ico', png: 'hint_png', svg: 'hint_svg' };
            const key = hintKeys[formatSelect.value];
            if (key && dict[key]) exportHint.textContent = dict[key];
        }
    }

    window.updateI18n = function() {
        updateLanguage(langSelect.value);
    }
});
